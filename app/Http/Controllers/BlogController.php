    public function destroy($id)
    {
        $query = Blog::find($id);
        if (is_null($query)) {
            return $this->errorResponse('Data not found.', Response::HTTP_NOT_FOUND);
        }
        $query->delete();

        return $this->successResponse($query, 'Data successfully deleted!', Response::HTTP_OK);
    }
}
